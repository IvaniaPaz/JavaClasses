package com.class07;

public class IncrementAndDecrement {

	public static void main(String[] args) {
	
				int a=10;
				
				a=a+1; //assignment
				a+=1; //shorthand assignment
				
				System.out.println(a);
				a++; //increment operator
				System.out.println(a);

			int b=10;
			b-=1;	//9
			b--; //decrement operator
			System.out.println(b);
		// with increment and decrement operators can be used with variables
		// only we can use:
				int c=23;
				c++;
				//1++; //complier error invalid argument
			}
	}
